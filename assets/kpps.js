//DPT LAKI LAKI
function clrDptL() {
  document.getElementById('valueDptL').innerHTML = '0';
}

function addDptL() {
  var result = parseFloat(document.getElementById('valueDptL').innerHTML) + 1;

  document.getElementById('valueDptL').innerHTML = result;
}

function delDptL() {
  var result = parseFloat(document.getElementById('valueDptL').innerHTML) - 1;

  if (result < 0) {
    document.getElementById('valueDptL').innerHTML = 0;
  } else {
    document.getElementById('valueDptL').innerHTML = result;
  }
}

